.MODEL SMALL
.STACK 100H
.DATA

	STRING1 DB'SACHIN TENDULKAR','$'

.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX

CALL REVERSE

	LEA DX,STRING1
	MOV AH,09H
	INT 21H

	MOV AH,4CH
	INT 21H

MAIN ENDP
REVERSE PROC

	MOV SI,OFFSET STRING1
	MOV CX,0H

L1:	MOV AX,[SI]
	CMP AL,'$'
	JE LABEL1
	PUSH[SI]
	INC SI
	INC CX
	JMP L1

LABEL1:
	MOV SI,OFFSET STRING1

L2:
	CMP CX,0
	JE EXIT
	POP DX
	XOR DH,DH
	MOV [SI],DX
	INC SI
	DEC CX
	JMP L2
EXIT:
	MOV [SI],'$ '
	RET

REVERSE ENDP
END MAIN
