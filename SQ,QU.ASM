.MODEL SMALL
.STACK 64H
.DATA
	MSG1 DB 10,13,"ENTER THE NUMBER:$"
	MSG2 DB 10,13,"THE SQUARE IS:$"
.CODE
.STARTUP
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H

	SUB AL,30H
	MOV BL,AL

	MUL BL
	MUL BL
	MOV DX,00
	MOV BX,10
	MOV CL,00

L1:	MOV DX,00
	DIV BX
	PUSH DX
	INC CL
	OR AX,AX
	JNZ L1

	LEA DX,MSG2
	MOV AH,09H
	INT 21H

L2:	POP DX
	ADD DX,30H
	MOV AH,02H
	INT 21H
	LOOP L2
	
	MOV AH,4CH
	INT 21H
END
