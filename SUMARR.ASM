.MODEL SMALL
.STACK 64H
.DATA

	ARRAY DB 0,1,2,3,4
	MSG1 DB 10,13,'THE SUM IS:$'

.CODE
.STARTUP

	MOV AX,@DATA
	MOV DS,AX

	MOV CX,04
	LEA SI,[ARRAY]
	MOV AL,[SI]

L1:	INC SI
	ADD AL,[SI]
	LOOP L1

	MOV DX,00
	MOV BX,10
	MOV CL,00
	MOV AH,00

L2:	MOV DX,00
	DIV BX
	PUSH DX
	INC CL
	OR AX,AX
	JNZ L2

	LEA DX,MSG1
	MOV AH,09H 
	INT 21H

L3:	POP DX
	ADD DX,30H
	MOV AH,02H
	INT 21H
	LOOP L3
	
	MOV AH,4CH
	INT 21H

END
