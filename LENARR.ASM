.MODEL SMALL
.STACK 64H
.DATA

	ARRAY DB 00,04,09,07,05
	MSG1 DB 10,13,'THE LARGEST NO IN ARRAY IS:$'

.CODE
.STARTUP

	MOV AX,@DATA
	MOV DS,AX

	LEA DX,MSG1
	MOV AH,09H
	INT 21H

	LEA SI,ARRAY
	MOV CX,04
	MOV AL,[SI]

L1:	INC SI
	CMP AL,[SI]
	JA J2
	LOOP L1

	ADD AL,30H
	MOV DL,AL

	MOV AH,02H
	INT 21H
	JMP J3

J2:	MOV AL,[SI]
	LOOP L1

J3:	MOV AH,4CH 
	INT 21H 

END
