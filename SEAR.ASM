.MODEL SMALL
.STACK 64H
.DATA

	ARRAY DB 0,4,5,6,2
	MSG1 DB 10,13,'ENTER THE ELEMENT U WANT TO SEARCH:$'
	MSG2 DB 10,13,'SEARCHED ELEMENT FOUND$'
	MSG3 DB 10,13,'SEARCHED ELEMENT NOT FOUND$'

.CODE
.STARTUP
	
	MOV AX,@DATA
	MOV DS,AX

	LEA DX,MSG1
	MOV AH,09H
	INT 21H

	MOV AH,01H
	INT 21H

	SUB AL,30H
	MOV BL,AL
	
	LEA SI,ARRAY
	MOV CX,04

L1:	MOV AL,[SI]
	CMP AL,BL
	JZ M1
	INC SI
	LOOP L1

	LEA DX,MSG3
	MOV AH,09H
	INT 21H

	JMP M2

M1:	LEA DX,MSG2
	MOV AH,09H
	INT 21H

M2:	MOV AH,4CH
	INT 21H

END
