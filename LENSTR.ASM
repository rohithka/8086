.MODEL SMALL
.STACK 64H
.DATA
	STR1 DB 'SACHIN$'
	MSG2 DB 10,13,'THE LENGTH IS:$'

.CODE
.STARTUP

	MOV AX,@DATA
	MOV DS,AX

	MOV CL,00
	LEA SI,STR1

L1:	MOV AL,[SI]
	CMP AL,'$'

	JZ L2
	INC CL
	INC SI
	JMP L1

	



L2:	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	MOV DL,CL
	ADD DL,30H
	MOV AH,02H
	INT 21H


	

	
	MOV AH,4CH
	INT 21H

END
