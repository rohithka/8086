.MODEL SMALL
.STACK 100H
.DATA
	MSG1 DB "THE FIRST STRING IS: $"
	MSG2 DB 10,13,"THE COPIED STRING IS:$"
	STRING1 DB"HELLO WORLD",'$'
	STRING2 DB 15 DUP(?),'$'
.CODE
.STARTUP
	MOV AX,@DATA
	MOV DS,AX
	MOV SI,00
	MOV CL,15
L1:	MOV BL,STRING1[SI]
	MOV STRING2[SI],BL
	INC SI
	DEC CL
	JNZ L1
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	LEA DX,STRING2
	MOV AH,09H
	INT 21H
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	LEA DX,STRING2
	MOV AH,09H
	INT 21H
	MOV AH,4CH
	INT 21H
END
